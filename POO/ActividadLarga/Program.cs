El hotel reconoce dos temporadas principales: alta y baja.
Durante la temporada alta, las tarifas de las habitaciones deben aumentar un 50% sobre la tarifa base debido a la alta demanda.
En temporada baja, las tarifas deben mantenerse en su tarifa base.

El hotel también ofrece varios servicios adicionales para mejorar la experiencia de los huéspedes.
Algunos de estos servicios son el desayuno incluido, el traslado al aeropuerto y el acceso al spa.
