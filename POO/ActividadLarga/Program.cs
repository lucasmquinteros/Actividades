Las habitaciones pueden tener varios servicios asociados, y estos servicios deben ser seleccionados al momento de realizar la reserva.

Para procesar el pago de las reservas, el hotel acepta varios métodos de pago, como tarjeta de crédito, efectivo, y transferencia bancaria.
Es importante que el sistema registre el método de pago utilizado para cada reserva, ya que esto influye en el proceso contable del hotel.

El Sr. Hernández también desea que el sistema sea capaz de gestionar a sus clientes de manera eficiente.
Cada cliente que haga una reserva debe estar registrado en el sistema con su nombre y documento de identidad.
Una vez registrado, un cliente puede realizar reservas en el hotel especificando la habitación que desea, los servicios adicionales, la temporada y el método de pago.
Es importante que el sistema facilite la creación de reservas y permita calcular el costo total de la estancia de un cliente, incluyendo tanto el costo de la habitación como los servicios adicionales seleccionados.

Ejemplo de escenario:
- Cliente: Juan Pérez
- Habitación solicitada: Suite
- Servicios adicionales: Desayuno incluido y traslado al aeropuerto
- Temporada: Alta
- Método de pago: Tarjeta de crédito

Consideraciones adicionales:
- Las habitaciones del hotel pueden existir independientemente de las reservas, pero cada reserva debe estar vinculada a una habitación específica.
- Los servicios adicionales son específicos para cada habitación y no pueden existir sin una habitación que los contenga.
- Los clientes pueden realizar múltiples reservas, pero cada reserva debe ser única y estar asociada a un solo cliente.
- La aplicación debe proporcionar informes claros y precisos sobre las reservas y los pagos realizados.
